func Triangle3(s string) rune {
	a := []rune(s)
	for b := make([]rune, 0, len(a)-1); len(a) != 1; b = b[:0] {
		for i := range a[1:] {
			b = append(b, rgb(a[i], a[i+1]))
		}
		a, b = b, a
	}
	return a[0]
}
func rgb(a, b rune) rune {
	switch {
	case a == b:		return a
	case a == 'B' && b == 'G' || b == 'B' && a == 'G':		return 'R'
	case a == 'R' && b == 'B' || b == 'R' && a == 'B':		return 'G'
	}
	return 'B'
}

func Triangle4(row string) rune {
	rs := []rune(row)
	for len(rs) > 1 {
		for i := 0; i < len(rs)-1; i++ {
			switch s := string(rs[i : i+2]); s {
			case "GB", "BG":
				rs[i] = 'R'
			case "RB", "BR":
				rs[i] = 'G'
			case "RG", "GR":
				rs[i] = 'B'
			}
		}
		rs = rs[:len(rs)-1]
	}
	return rs[0]
}

func Triangle5(row string) rune {
	var rgb = map[rune]map[rune]rune{}
	rgb['R'] = map[rune]rune{}
	rgb['R']['R'] = 'R'
	rgb['R']['G'] = 'B'
	rgb['R']['B'] = 'G'
	rgb['G'] = map[rune]rune{}
	rgb['G']['R'] = 'B'
	rgb['G']['G'] = 'G'
	rgb['G']['B'] = 'R'
	rgb['B'] = map[rune]rune{}
	rgb['B']['R'] = 'G'
	rgb['B']['G'] = 'R'
	rgb['B']['B'] = 'B'
	for len(row) != 1 {
	  var row2 = make([]rune, len(row)-1)
	  for i := 0; i < len(row)-1; i++ {
		row2[i] = rgb[rune(row[i])][rune(row[i+1])]
	  }
	  row = string(row2)
	}
	return rune(row[0])
  }

